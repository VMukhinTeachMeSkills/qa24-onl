package lesson1;

import java.util.Scanner;

public class Main
{
    public static void main(String[] args)
    {
        System.out.println("i------------------------------------------------");
        int i = 5;
        while (i < 5)
        {
            System.out.println(i);
            i++;
        }

        System.out.println("j------------------------------------------------");
        int j = 5;
        do
        {
            System.out.println(j);
            j++;
        } while (j < 5);
    }
}

